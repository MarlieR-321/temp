package entidades;

public class OpcionRol {
	private int id_opcion_rol;
	private int id_opcion;
	private String nombre_opcion;
	private int id_rol;
	private String nombre_rol;
	
	
	public int getId_opcion_rol() {
		return id_opcion_rol;
	}
	public void setId_opcion_rol(int id_opcion_rol) {
		this.id_opcion_rol = id_opcion_rol;
	}
	public int getId_opcion() {
		return id_opcion;
	}
	public void setId_opcion(int id_opcion) {
		this.id_opcion = id_opcion;
	}
	public int getId_rol() {
		return id_rol;
	}
	public void setId_rol(int id_rol) {
		this.id_rol = id_rol;
	}
	public String getNombre_opcion() {
		return nombre_opcion;
	}
	public void setNombre_opcion(String nombre_opcion) {
		this.nombre_opcion = nombre_opcion;
	}
	public String getNombre_rol() {
		return nombre_rol;
	}
	public void setNombre_rol(String nombre_rol) {
		this.nombre_rol = nombre_rol;
	}
		
}
